def unpack_position(packed):
    if packed < 0:
        packed += 1 << 64
    x = (packed >> 38) & 0x3FFFFFF
    y = (packed >> 26) & 0xFFF
    z = packed & 0x3FFFFFF
    if x >= 0x2000000:
        x -= 0x4000000
    if z >= 0x2000000:
        z -= 0x4000000
    return x, y, z

