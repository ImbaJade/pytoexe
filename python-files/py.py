Player1 = int(input("Введите ваше первое число!: "))
Player2 = int(input("Введите ваше второе число!: "))

print("Result: ", Player1 + Player2)
print("Result: ", Player1 - Player2)
print("Result: ", Player1 * Player2)
print("Result: ", Player1 / Player2)
print("Result: ", Player1 // Player2)
print("Result: ", Player1 ** Player2)