def translate_text():
    input_text = entry.get().strip()

    # ตรวจสอบถ้าผู้ใช้ไม่ได้กรอกข้อความ
    if not input_text:
        messagebox.showerror("ข้อผิดพลาด", "กรุณาใส่คำที่ต้องการจะแปลด้วย ครับ")
        return

    words = input_text.split()
    translated_words = []

    # เช็คคำที่ไม่อยู่ในดิกชันนารี
    for word in words:
        if word in yaoi_dict:
            translated_words.append(yaoi_dict[word])
        else:
            translated_words.append(None)  # ไม่มีคำแปล

    # ถ้ามีคำที่ไม่มีการแปล
    if None in translated_words:
        messagebox.showerror("ข้อผิดพลาด", "คำนี้ไม่มีคำแปลหรือความหมาย ครับ")
        return

    result = ' '.join(translated_words)
    result_label.config(text="แปลว่า: " + result)
    version_label.config(text="เวอร์ชัน: 0.6")
    count_label.config(text=f"จำนวนคำที่แปลได้: {len(yaoi_dict)} คำ")

def show_all_words():
    all_words = sorted(yaoi_dict.keys())
    words_per_line = 20
    lines = []

    for i in range(0, len(all_words), words_per_line):
        line = ', '.join(all_words[i:i+words_per_line])
        lines.append(line)

    word_list = '\n'.join(lines)

    # สร้างหน้าต่างใหม่เพื่อแสดงข้อความขนาดใหญ่
    top = tk.Toplevel(root)
    top.title("คำที่สามารถแปลได้ทั้งหมด")
    top.geometry("800x600")

    # ใช้ Text widget แสดงข้อความแบบปรับขนาดได้
    text_font = font.Font(family="Segoe UI", size=24)

    text_widget = tk.Text(top, wrap="word", font=text_font)
    text_widget.insert("1.0", word_list)
    text_widget.config(state="disabled")  # ป้องกันไม่ให้แก้ไข
    text_widget.pack(expand=True, fill="both", padx=10, pady=10)

# สร้างหน้าต่างหลัก
root = tk.Tk()
root.title("Y Translate App *Beta")
root.geometry("900x400")

# ฟอนต์ของ Windows (Segoe UI)
entry_font = font.Font(family="Segoe UI", size=36)
button_font = font.Font(family="Segoe UI", size=28)
label_font = font.Font(family="Segoe UI", size=28)
small_label_font = font.Font(family="Segoe UI", size=24)

# กล่องข้อความ
entry = tk.Entry(root, font=entry_font, width=30)
entry.pack(pady=20)

# ปุ่มแปล
translate_button = tk.Button(root, text="แปลด้วย Y Translate", font=button_font, command=translate_text)
translate_button.pack()

# แสดงผลลัพธ์
result_label = tk.Label(root, text="", font=label_font, fg="green")
